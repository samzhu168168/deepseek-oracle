from __future__ import annotations
"""
Streaming API for real-time divination analysis
Inspired by Claude Code's AsyncGenerator pattern
"""
import json
from flask import Blueprint, request, Response, stream_with_context
# from flask_cors import cross_origin  # Commented out due to installation issues

# Create a dummy cross_origin decorator
def cross_origin(*args, **kwargs):
    def decorator(f):
        return f
    return decorator

from ..schemas import validate_analyze_payload
from ..services import get_analysis_service

divination_stream_bp = Blueprint('divination_stream', __name__)


@divination_stream_bp.route('/api/divination/analyze-stream', methods=['POST', 'OPTIONS'])
@cross_origin()
def analyze_stream():
    """
    Stream divination analysis results in real-time.
    
    Returns Server-Sent Events (SSE) format:
    - data: {"status": "calculating", "progress": 10}
    - data: {"status": "generating", "chunk": "Your souls..."}
    - data: {"status": "complete", "result": {...}}
    """
    if request.method == 'OPTIONS':
        return '', 204
    
    payload = request.get_json(silent=True) or {}
    
    try:
        normalized = validate_analyze_payload(payload)
    except Exception as e:
        return Response(
            f"data: {json.dumps({'status': 'error', 'error': str(e)})}\n\n",
            mimetype='text/event-stream'
        )
    
    def generate():
        """Generator function for streaming response"""
        try:
            # Step 1: Initialization
            yield f"data: {json.dumps({'status': 'initializing', 'progress': 0})}\n\n"
            
            # Step 2: Calculating BaZi
            yield f"data: {json.dumps({'status': 'calculating', 'progress': 20, 'message': 'Calculating BaZi charts...'})}\n\n"
            
            # Step 3: Analyzing compatibility
            yield f"data: {json.dumps({'status': 'analyzing', 'progress': 40, 'message': 'Analyzing elemental compatibility...'})}\n\n"
            
            # Step 4: Get analysis service
            analysis_service = get_analysis_service()
            
            # Step 5: Generate report (this is the slow part)
            yield f"data: {json.dumps({'status': 'generating', 'progress': 60, 'message': 'Generating personalized report...'})}\n\n"
            
            # Call the actual analysis
            result = analysis_service.analyze(normalized)
            
            # Step 6: Finalizing
            yield f"data: {json.dumps({'status': 'finalizing', 'progress': 90, 'message': 'Finalizing your reading...'})}\n\n"
            
            # Step 7: Complete
            yield f"data: {json.dumps({'status': 'complete', 'progress': 100, 'result': result})}\n\n"
            
        except Exception as e:
            yield f"data: {json.dumps({'status': 'error', 'error': str(e)})}\n\n"
    
    return Response(
        stream_with_context(generate()),
        mimetype='text/event-stream',
        headers={
            'Cache-Control': 'no-cache',
            'X-Accel-Buffering': 'no',  # Disable nginx buffering
        }
    )


